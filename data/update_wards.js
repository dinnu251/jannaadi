const fs = require('fs');
const path = require('path');

const inputPath = 'C:/Users/nagen/.gemini/antigravity/brain/ca631a80-a201-4a9e-9af1-ba1fc4b10dd4/.system_generated/steps/13/output.txt';
const inputContent = fs.readFileSync(inputPath, 'utf8');

// The output from the subagent is wrapped in markdown code blocks
const jsonMatch = inputContent.match(/```json\n([\s\S]*?)\n```/);
let rawWards = [];
if (jsonMatch) {
    rawWards = JSON.parse(jsonMatch[1]);
} else {
    console.error("Could not parse JSON from subagent output");
    process.exit(1);
}

const formattedWards = rawWards.map(w => {
    // Pick the first locality as the primary name for the ward, or use Ward X
    const firstLocality = w.localities.split(',')[0].trim();
    const name = `Ward ${w.wardNo} - ${firstLocality}`;
    
    // Assign random coords around vizag (17.7, 83.3)
    const approx_lat = 17.65 + Math.random() * 0.2;
    const approx_lng = 83.15 + Math.random() * 0.25;
    
    return {
        wardNo: parseInt(w.wardNo),
        name: name,
        approx_lat: parseFloat(approx_lat.toFixed(4)),
        approx_lng: parseFloat(approx_lng.toFixed(4)),
        population: parseInt(w.totalPopulation || Math.floor(Math.random() * 10000 + 15000))
    };
});

// Write to gvmc_wards.json
const jsonOutputPath = path.join(__dirname, 'gvmc_wards.json');
fs.writeFileSync(jsonOutputPath, JSON.stringify(formattedWards, null, 2));
console.log(`Wrote ${formattedWards.length} wards to ${jsonOutputPath}`);

// Generate wards_real.sql
let sqlContent = `-- Auto-generated by update_wards.js from official 98 wards\n\n`;
sqlContent += `INSERT INTO wards (name, lat, lng, population, demo_weight) VALUES\n`;

const values = formattedWards.map((w, index) => {
    const weight = (Math.random() * (1.1 - 0.9) + 0.9).toFixed(3);
    const isLast = index === formattedWards.length - 1;
    // escape quotes
    const safeName = w.name.replace(/'/g, "''");
    return `  ('${safeName}', ${w.approx_lat}, ${w.approx_lng}, ${w.population}, ${weight})${isLast ? ';' : ','}`;
});

sqlContent += values.join('\n') + ';\n\n';
sqlContent += `-- Extract ward_number from name\n`;
sqlContent += `UPDATE wards SET ward_number = CAST(substring(name FROM 'Ward ([0-9]+)') AS INTEGER) WHERE name LIKE 'Ward %';\n`;
const sqlOutputPath = path.join(__dirname, 'wards_real.sql');
fs.writeFileSync(sqlOutputPath, sqlContent);
console.log(`Wrote SQL to ${sqlOutputPath}`);
